
public class WhileLoopsEvenNumbers {
	public static void main(String[] args) {
		int i =1, y=1;
		while(i<=100) {
			if (i%2==0)
				System.out.println(i);		 
		++i;
			
		}
		
		System.out.println("******************OR***********************");
		
		while(y<=100) {
			if (y%2==0)
				System.out.print(y+" ");
		++y;	
		}
	}
}
